nome = input('Insira o seu nome completo \n>>')
prim_nome = nome.split()
ultimo_nome = nome.split()
print(f'O seu primeiro nome é {prim_nome[0]}')
for i in range(len(ultimo_nome[0])):
    last_name = ultimo_nome[i - i - 1]
print(f'O seu ultimo nome é {last_name}')
print(f'O seu ultimo nome é {ultimo_nome[-1]}')
